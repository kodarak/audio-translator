def get_language_name(code: str, lang: str = 'uk') -> str:
    code = code.lower()
    name_key = 'uk_name' if lang == 'uk' else 'name'
    
    for lang_code, lang_info in LANGUAGE_CODES.items():
        if code == lang_code or code in lang_info['codes']:
            return lang_info[name_key]
    
    return LANGUAGE_CODES['unknown'][name_key]

def get_language_family(code: str, lang: str = 'uk') -> str:
    FAMILY_NAMES = {
        'Indo-European': 'Індоєвропейська',
        'Germanic': 'Германська',
        'Romance': 'Романська',
        'Slavic': 'Слов\'янська',
        'Celtic': 'Кельтська',
        'Baltic': 'Балтійська',
        'Turkic': 'Тюркська',
        'Semitic': 'Семітська',
        'Sino-Tibetan': 'Сино-тибетська',
        'Austronesian': 'Австронезійська',
        'Austroasiatic': 'Австроазійська',
        'Niger-Congo': 'Нігеро-конголезька',
        'Afroasiatic': 'Афразійська',
        'Constructed': 'Штучна',
        'Unknown': 'Невідома'
    }
    
    code = code.lower()
    for lang_code, lang_info in LANGUAGE_CODES.items():
        if code == lang_code or code in lang_info['codes']:
            family = lang_info.get('family', 'Unknown')
            return FAMILY_NAMES.get(family, family) if lang == 'uk' else family
    
    return FAMILY_NAMES['Unknown'] if lang == 'uk' else 'Unknown'

def get_all_families(lang: str = 'uk') -> dict:
    families = {}
    for code, info in LANGUAGE_CODES.items():
        if code == 'unknown':
            continue
        family = get_language_family(code, lang)
        if family not in families:
            families[family] = []
        families[family].append(get_language_name(code, lang))
    return {k: sorted(v) for k, v in families.items()}